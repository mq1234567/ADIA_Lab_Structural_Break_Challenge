"""K-fold cross-validation BY SERIES for the structural-break challenge.
e.g. In total 10 unique series, 3 folds: 
- fold 0 trains on series 2,3,4,5,6,7,8 and validates on 0,1,9
- fold 1 trains on series 0,1,4,5,6,7,8 and validates on 2,3,9
- fold 2 trains on series 0,1,2,3,6,7,8 and validates on 4,5,9
The folds are generated by `series_folds()`, which can be seeded for reproducibility.


Typical use
-----------
    import cv_tools as cv

    folds = cv.series_folds(np.unique(row_sid), n_splits=5, seed=0)
    res_lgb = cv.run_cv(X, y, row_sid, row_index, cv.sklearn_fit_predict(make_lgb),
                        folds=folds, train_row_mask=is_sampled_step)
    res_cat = cv.run_cv(X, y, row_sid, row_index, cv.sklearn_fit_predict(make_cat),
                        folds=folds, train_row_mask=is_sampled_step)
    print(cv.summarize({"LGB": res_lgb, "Cat": res_cat}))
    print(cv.paired_compare(res_cat, res_lgb, "Cat", "LGB"))

`fit_predict` receives two `Split` objects (train, val) carrying X / y / weights /
series ids / online-step index, so ranking objectives that need per-step groups
(LGBMRanker lambdarank, CatBoost YetiRank) are supported without changing this API.
"""
from __future__ import annotations

import os
import sys
from dataclasses import dataclass
from typing import Callable, Iterable, List, Optional, Sequence, Tuple

import numpy as np
import pandas as pd

import tools

# Helper functions

def ts_auc(pred: pd.Series, y_true: pd.Series) -> float:
    """TS-AUC via tools.local_ts_auc, as (id, time)-indexed Series.

    local_ts_auc derives the per-step strata with `groupby("id").cumcount()`, so it
    depends on row ORDER — both sides are sorted here to make the result independent
    of however the caller assembled the rows.
    """
    p = pd.Series(pred).rename("prediction").sort_index().to_frame()
    yt = pd.Series(y_true).rename("target").sort_index().to_frame()
    return float(tools.local_ts_auc(yt, p))


def rank01(a: np.ndarray) -> np.ndarray:
    """Map scores to [0, 1] ranks — used to average models before scoring."""
    a = np.asarray(a)
    if len(a) < 2:
        return np.zeros(len(a), dtype=np.float64)
    return np.argsort(np.argsort(a)) / (len(a) - 1.0)


# ---------------------------------------------------------------------------
# data containers
# ---------------------------------------------------------------------------
@dataclass
class Split:
    """One side (train or val) of a fold."""
    X: np.ndarray                 # [n_rows, n_features]
    y: np.ndarray                 # [n_rows] 0/1
    w: np.ndarray                 # [n_rows] sample weights (equal-series by default)
    sid: np.ndarray               # [n_rows] series id per row
    step: np.ndarray              # [n_rows] online step index (0-based) per row
    index: pd.MultiIndex          # [n_rows] (id, time) — for TS-AUC alignment

    def __len__(self) -> int:
        return len(self.y)


@dataclass
class CVResult:
    """Per-fold TS-AUCs plus pooled out-of-fold predictions."""
    fold_scores: np.ndarray       # [n_folds]
    oof_pred: pd.Series           # prediction for every row, indexed (id, time)
    oof_score: float              # TS-AUC over all OOF predictions pooled
    n_folds: int

    @property
    def mean(self) -> float:
        return float(np.mean(self.fold_scores))

    @property
    def std(self) -> float:
        return float(np.std(self.fold_scores, ddof=1)) if self.n_folds > 1 else 0.0

    @property
    def sem(self) -> float:
        return self.std / np.sqrt(self.n_folds) if self.n_folds > 1 else 0.0

    def summary(self, name: str = "model") -> str:
        folds = "  ".join(f"{s:.4f}" for s in self.fold_scores)
        return (f"{name:<18s} mean {self.mean:.4f} +/- {self.std:.4f} (sem {self.sem:.4f})"
                f" | OOF {self.oof_score:.4f} | folds: {folds}")


# ---------------------------------------------------------------------------
# folding
# ---------------------------------------------------------------------------
def series_folds(series_ids: Sequence, n_splits: int = 5, seed: int = 0
                 ) -> List[Tuple[np.ndarray, np.ndarray]]:
    """Split UNIQUE series ids into `n_splits` (train_ids, val_ids) pairs."""
    ids = np.asarray(sorted(set(np.asarray(series_ids).tolist())))
    rng = np.random.default_rng(seed)
    ids = ids[rng.permutation(len(ids))]
    chunks = np.array_split(ids, n_splits)
    folds = []
    for k in range(n_splits):
        val = np.sort(chunks[k])
        train = np.sort(np.concatenate([chunks[j] for j in range(n_splits) if j != k]))
        folds.append((train, val))
    return folds


def equal_series_weights(sid: np.ndarray) -> np.ndarray:
    """1 / (#rows for that series), normalised to mean 1 — each SERIES counts equally.

    Matches the deployed trainer. Without it, long series dominate the loss purely by
    having more rows, which is not what TS-AUC rewards.
    """
    uniq, counts = np.unique(sid, return_counts=True)
    cmap = dict(zip(uniq.tolist(), counts.tolist()))
    w = np.array([1.0 / cmap[s] for s in sid.tolist()], dtype=np.float64)
    return w / w.mean()


def steps_from_index(index: pd.MultiIndex) -> np.ndarray:
    """0-based online step per row = per-series cumcount (index assumed (id, time)-sorted)."""
    return pd.Series(0, index=index).groupby(level="id").cumcount().to_numpy()


# ---------------------------------------------------------------------------
# fit_predict adapters
# ---------------------------------------------------------------------------
FitPredict = Callable[["Split", "Split"], np.ndarray]


def sklearn_fit_predict(factory: Callable[[], object], use_weights: bool = True) -> FitPredict:
    """Wrap an sklearn-style estimator factory into a `fit_predict`.

    `factory()` must return a fresh, unfitted estimator exposing
    `fit(X, y, sample_weight=...)` and `predict_proba(X)[:, 1]`.
    """
    def _fp(train: Split, val: Split) -> np.ndarray:
        model = factory()
        if use_weights:
            model.fit(train.X, train.y, sample_weight=train.w)
        else:
            model.fit(train.X, train.y)
        return model.predict_proba(val.X)[:, 1]
    return _fp


# ---------------------------------------------------------------------------
# the CV driver
# ---------------------------------------------------------------------------
def run_cv(X: np.ndarray,
           y: np.ndarray,
           row_sid: np.ndarray,
           row_index: pd.MultiIndex,
           fit_predict: FitPredict,
           folds: Optional[List[Tuple[np.ndarray, np.ndarray]]] = None,
           n_splits: int = 5,
           seed: int = 0,
           train_row_mask: Optional[np.ndarray] = None,
           row_step: Optional[np.ndarray] = None,
           verbose: bool = True) -> CVResult:
    """K-fold CV by series. Returns per-fold TS-AUC + pooled OOF predictions.

    Parameters
    ----------
    X, y, row_sid, row_index
        Row-aligned feature matrix, 0/1 labels, series id per row, and the
        (id, time) MultiIndex used for TS-AUC alignment.
    fit_predict
        `f(train: Split, val: Split) -> pred[len(val)]`. Use `sklearn_fit_predict`
        for ordinary estimators; write your own for rankers needing per-step groups.
    folds
        Pass folds from `series_folds` to compare models on IDENTICAL splits
        (required for `paired_compare`). If None, they are generated here.
    train_row_mask
        Boolean over rows marking rows eligible for TRAINING — e.g. the 40 log-spaced
        steps the deployed trainer samples. Validation always uses ALL rows of the
        held-out series, because that is what TS-AUC scores. Default: all rows.
    row_step
        Online step per row; derived from `row_index` if omitted.
    """
    X = np.asarray(X)
    y = np.asarray(y).astype(np.int8)
    row_sid = np.asarray(row_sid)
    if not isinstance(row_index, pd.MultiIndex):
        raise TypeError("row_index must be an (id, time) MultiIndex")
    if not (len(X) == len(y) == len(row_sid) == len(row_index)):
        raise ValueError("X, y, row_sid, row_index must be row-aligned")

    step = steps_from_index(row_index) if row_step is None else np.asarray(row_step)
    train_row_mask = (np.ones(len(y), bool) if train_row_mask is None
                      else np.asarray(train_row_mask).astype(bool))
    if folds is None:
        folds = series_folds(row_sid, n_splits=n_splits, seed=seed)

    y_true_all = pd.Series(y, index=row_index)
    oof = np.full(len(y), np.nan, dtype=np.float64)
    scores = []

    for k, (tr_ids, va_ids) in enumerate(folds):
        in_tr = np.isin(row_sid, tr_ids) & train_row_mask     # subsampled steps
        in_va = np.isin(row_sid, va_ids)                      # ALL steps
        if in_tr.sum() == 0 or in_va.sum() == 0:
            raise ValueError(f"fold {k}: empty train or val split")

        train = Split(X[in_tr], y[in_tr], equal_series_weights(row_sid[in_tr]),
                      row_sid[in_tr], step[in_tr], row_index[in_tr])
        val = Split(X[in_va], y[in_va], equal_series_weights(row_sid[in_va]),
                    row_sid[in_va], step[in_va], row_index[in_va])

        pred = np.asarray(fit_predict(train, val), dtype=np.float64).ravel()
        if len(pred) != len(val):
            raise ValueError(f"fold {k}: fit_predict returned {len(pred)} preds "
                             f"for {len(val)} val rows")
        oof[in_va] = pred

        s = float(ts_auc(pd.Series(pred, index=val.index), y_true_all[in_va]))
        scores.append(s)
        if verbose:
            print(f"  fold {k}: train {in_tr.sum():>9,} rows / {len(tr_ids):>5,} series | "
                  f"val {in_va.sum():>9,} rows / {len(va_ids):>5,} series | TS-AUC {s:.4f}",
                  flush=True)

    oof_pred = pd.Series(oof, index=row_index)
    oof_score = float(ts_auc(oof_pred, y_true_all))
    return CVResult(np.asarray(scores), oof_pred, oof_score, len(folds))


# ---------------------------------------------------------------------------
# reporting
# ---------------------------------------------------------------------------
def paired_compare(res_a: CVResult, res_b: CVResult,
                   name_a: str = "A", name_b: str = "B") -> str:
    """Compare two CVResults computed on the SAME folds.

    Pairing matters: folds differ in difficulty and that shared variance cancels in
    the per-fold differences. A +0.002 mean gap is solid when every fold agrees and
    meaningless when they disagree — only the paired spread distinguishes the two.
    """
    if res_a.n_folds != res_b.n_folds:
        raise ValueError("results have different fold counts — rerun with shared folds")
    d = res_a.fold_scores - res_b.fold_scores
    mean = float(np.mean(d))
    std = float(np.std(d, ddof=1)) if len(d) > 1 else 0.0
    sem = std / np.sqrt(len(d)) if len(d) > 1 else 0.0
    wins = int((d > 0).sum())
    if sem > 0:
        t = mean / sem
    else:
        t = float("inf") if mean > 0 else (float("-inf") if mean < 0 else 0.0)
    verdict = ("consistent" if wins in (0, len(d)) and abs(t) >= 2
               else "likely" if abs(t) >= 2
               else "within noise")
    per_fold = "  ".join(f"{x:+.4f}" for x in d)
    return (f"{name_a} - {name_b}: {mean:+.4f} +/- {std:.4f} (sem {sem:.4f}, "
            f"t {t:+.2f}, wins {wins}/{len(d)}) -> {verdict}\n"
            f"  per-fold deltas: {per_fold}")


def summarize(results: dict, sort: bool = True) -> str:
    """Format several named CVResults as a leaderboard block."""
    items = list(results.items())
    if sort:
        items.sort(key=lambda kv: -kv[1].mean)
    return "\n".join(r.summary(n) for n, r in items)


def blend_oof(results: dict, y_true: pd.Series,
              names: Optional[Iterable[str]] = None) -> Tuple[float, pd.Series]:
    """Rank-average several models' OOF predictions -> (TS-AUC, blended Series).

    Blending on OOF predictions is the honest way: every member's prediction for a
    row came from a fold that did not train on that row, so the blend score is not
    inflated by members having memorised the rows they are being combined on.
    """
    names = list(results) if names is None else list(names)
    if not names:
        raise ValueError("no models to blend")
    idx = results[names[0]].oof_pred.index
    stack = []
    for n in names:
        p = results[n].oof_pred.reindex(idx)
        if p.isna().any():
            raise ValueError(f"{n}: OOF predictions incomplete — cannot blend")
        stack.append(rank01(p.to_numpy()))
    blended = pd.Series(np.mean(stack, axis=0), index=idx)
    return float(ts_auc(blended, y_true.reindex(idx))), blended
